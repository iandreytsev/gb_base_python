final_list = []
lower_case_alphabet = "abcdefghijklmnopqrstuvwxyz"

for symbol in line:
    if symbol in lower_case_alphabet:
        help_line += symbol  # собираем все маленькие буквы, пока не встретим заглавную
    # когда появляется первая заглавная буква, после маленькой, добавляем собранные буквы в список
